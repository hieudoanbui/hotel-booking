const fallbackTranslations = translations.vi;

function mergeTranslations(base, target) {
  const result = { ...base };

  Object.keys(target || {}).forEach((key) => {
    if (
      typeof target[key] === "object" &&
      target[key] !== null &&
      !Array.isArray(target[key])
    ) {
      result[key] = mergeTranslations(base[key] || {}, target[key]);
    } else {
      result[key] = target[key];
    }
  });

  return result;
}

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    return localStorage.getItem("siteLanguage") || "vi";
  });

  useEffect(() => {
    localStorage.setItem("siteLanguage", language);
    document.documentElement.lang = language;
  }, [language]);

  const currentLanguage =
    languages.find((item) => item.code === language) || languages[0];

  const t = useMemo(() => {
    return mergeTranslations(
      fallbackTranslations,
      translations[language] || fallbackTranslations
    );
  }, [language]);

  const value = useMemo(() => {
    return {
      language,
      setLanguage,
      currentLanguage,
      languages,
      t,
    };
  }, [language, currentLanguage, t]);

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);

  if (!context) {
    throw new Error("useLanguage must be used inside LanguageProvider");
  }

  return context;
}